package com.knitml.renderer.chart.symbol;

import com.knitml.renderer.common.RenderingException;

public class UnsupportedSymbolSetException extends RenderingException {

	private static final long serialVersionUID = 1L;

	public UnsupportedSymbolSetException() {
		super();
	}

	public UnsupportedSymbolSetException(String message, Throwable cause) {
		super(message, cause);
	}

	public UnsupportedSymbolSetException(String message) {
		super(message);
	}

	public UnsupportedSymbolSetException(Throwable cause) {
		super(cause);
	}
	
	

}
