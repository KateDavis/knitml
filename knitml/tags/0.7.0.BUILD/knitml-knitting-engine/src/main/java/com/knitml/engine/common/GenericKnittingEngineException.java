package com.knitml.engine.common;

public class GenericKnittingEngineException extends KnittingEngineException {

	/**
	 * 
	 */
	private static final long serialVersionUID = 7558548173812003965L;

	public GenericKnittingEngineException() {
	}

	public GenericKnittingEngineException(String message) {
		super(message);
	}

	public GenericKnittingEngineException(Throwable cause) {
		super(cause);
	}

	public GenericKnittingEngineException(String message, Throwable cause) {
		super(message, cause);
	}

}
